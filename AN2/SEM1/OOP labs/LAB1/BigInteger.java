public class Laboratory {
    public static void main(String[] args) {
		/*a)Adds an integral amount to the highest primitive value of each primitive type.*/
		System.out.println("For integer type :");
		int a=Integer.MAX_VALUE;
		a+=1;
        System.out.println(a);
		System.out.println("For float type :");
		float b=Float.MAX_VALUE;
		b+=1;
        System.out.println(b);
		System.out.println("For double type :");
		float c=Double.MAX_VALUE;
		c+=1;
        System.out.println(c);
		System.out.println("For long type :");
		float d=Long.MAX_VALUE;
		d+=1;
        System.out.println(d);
    }
}

